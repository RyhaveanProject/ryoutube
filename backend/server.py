def _normalize_cookies(raw: bytes) -> Optional[bytes]:
    try: text = raw.decode("utf-8", errors="ignore").strip()
    except Exception: return None
    if not text: return None
    first = "\n".join(text.splitlines()[:5])
    if first.startswith("# Netscape") or "# HTTP Cookie File" in first: return raw
    sample = next((ln for ln in text.splitlines() if ln and not ln.startswith("#")), "")
    if sample.count("\t") >= 6: return raw
    expiry = int(time.time() + 86400 * 365)
    out = ["# Netscape HTTP Cookie File", "# generated by ryoutube auto-converter"]
    if text.startswith("[") or text.startswith("{"):
        try:
            import json as _json
            data = _json.loads(text)
            if isinstance(data, dict): data = [data]
            for c in data:
                domain = c.get("domain") or ".youtube.com"
                if not domain.startswith("."): domain = "." + domain.lstrip(".")
                path = c.get("path", "/"); exp = int(c.get("expirationDate") or expiry)
                name = c.get("name", ""); value = c.get("value", "")
                if not name: continue
                out.append(f"{domain}\tTRUE\t{path}\tFALSE\t{exp}\t{name}\t{value}")
            if len(out) > 2: return ("\n".join(out) + "\n").encode("utf-8")
        except Exception: pass
    if ";" in text and "=" in text:
        if text.lower().startswith("cookie:"): text = text.split(":", 1)[1].strip()
        for pair in text.replace("\n", ";").split(";"):
            pair = pair.strip()
            if not pair or "=" not in pair: continue
            name, _, value = pair.partition("=")
            name, value = name.strip(), value.strip()
            if not name: continue
            out.append(f".youtube.com\tTRUE\t/\tFALSE\t{expiry}\t{name}\t{value}")
        if len(out) > 2: return ("\n".join(out) + "\n").encode("utf-8")
    return None

