def _mem_get(key):
    v = _MEM_CACHE.get(key)
    if not v: return None
    exp, data = v
    if time.time() > exp:
        _MEM_CACHE.pop(key, None); return None
    return data

def _mem_set(key, data, ttl):
    _MEM_CACHE[key] = (time.time() + ttl, data)

async def _db_cache_get(key):
    doc = await db.yt_cache.find_one({"_id": key})
    if not doc or doc.get("expires_at", 0) < time.time(): return None
    return doc.get("data")

async def _db_cache_set(key, data, ttl):
    await db.yt_cache.update_one({"_id": key},
        {"$set": {"data": data, "expires_at": time.time() + ttl,
                  "updated_at": datetime.now(timezone.utc).isoformat()}}, upsert=True)

async def cache_get(key):
    hit = _mem_get(key)
    if hit is not None: return hit
    hit = await _db_cache_get(key)
    if hit is not None:
        _mem_set(key, hit, 60 * 5)
        return hit
    return None

async def cache_set(key, data, ttl):
    _mem_set(key, data, min(ttl, 60 * 30))
    await _db_cache_set(key, data, ttl)

