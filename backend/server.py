def hashp(p): return bcrypt.hashpw(p.encode(), bcrypt.gensalt()).decode()
def verifyp(p, h):
    try: return bcrypt.checkpw(p.encode(), h.encode())
    except Exception: return False
def mktok(uid, did, role):
    return jwt.encode({"uid": uid, "did": did, "role": role,
        "exp": datetime.now(timezone.utc)+timedelta(days=JWT_EXPIRE_DAYS)},
        JWT_SECRET, algorithm=JWT_ALGO)
def dectok(t): return jwt.decode(t, JWT_SECRET, algorithms=[JWT_ALGO])

