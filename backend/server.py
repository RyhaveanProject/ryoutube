@api.get("/recommendations")
async def recommendations(video_id: Optional[str] = None, user=Depends(get_current_user)):
    queries: List[str] = []; seed_title = None
    if video_id:
        info = await db.play_counts.find_one({"id": video_id}, {"_id": 0, "title": 1, "channel": 1})
        if info:
            seed_title = info.get("title")
            if info.get("channel"): queries.append(info["channel"])
    history = await db.history.find({"user_id": user["id"]}, {"_id": 0}).sort("played_at", -1).to_list(5)
    for h in history:
        if h.get("channel"): queries.append(h["channel"])
    if not queries: queries = ["trending", "music", "news"]
    if seed_title: queries.insert(0, seed_title)
    seen = set(); final: List[dict] = []
    for q in queries[:3]:
        try:
            res = await yt_search(q, limit=12)
            for r in res:
                if r["id"] == video_id or r["id"] in seen: continue
                seen.add(r["id"]); final.append(r)
        except Exception: continue
    boosted = await db.trend_boosts.find({}, {"_id": 0}).sort("boost", -1).to_list(20)
    if boosted:
        ids = {b["video_id"] for b in boosted}
        bi = [v for v in final if v["id"] in ids]
        rest = [v for v in final if v["id"] not in ids]
        final = bi + rest
    return {"results": final[:30]}

