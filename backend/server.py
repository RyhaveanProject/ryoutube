# ==================== Background tasks ====================
async def _prune_cache_loop():
    while True:
        try:
            await asyncio.sleep(60 * 60)
            now = time.time()
            res = await db.yt_cache.delete_many({"expires_at": {"$lt": now}})
            if res.deleted_count:
                logger.info(f"[cache] pruned {res.deleted_count} expired entries")
        except asyncio.CancelledError: break
        except Exception as e:
            logger.warning(f"[cache] prune error: {e}")

async def _ensure_admin():
    existing = await db.users.find_one({"email": ADMIN_EMAIL.lower()})
    if existing:
        if not verify_password(ADMIN_PASSWORD, existing.get("password_hash", "")):
            await db.users.update_one({"id": existing["id"]},
                {"$set": {"password_hash": hash_password(ADMIN_PASSWORD),
                          "role": "admin", "active": True}})
        return
    doc = {"id": str(uuid.uuid4()), "email": ADMIN_EMAIL.lower(), "name": "Admin",
           "password_hash": hash_password(ADMIN_PASSWORD), "role": "admin",
           "active": True, "device_id": None,
           "created_at": datetime.now(timezone.utc).isoformat()}
    await db.users.insert_one(doc)
    logger.info("Admin user seeded: %s", ADMIN_EMAIL)

async def _ensure_indexes():
    try:
        await db.yt_cache.create_index("expires_at")
        await db.users.create_index("email", unique=True)
        await db.history.create_index([("user_id", 1), ("played_at", -1)])
        await db.likes.create_index([("user_id", 1), ("video_id", 1)], unique=True)
        await db.watch_later.create_index([("user_id", 1), ("video_id", 1)], unique=True)
    except Exception as e:
        logger.warning(f"index creation: {e}")

