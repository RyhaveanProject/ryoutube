def _pick_thumb(thumbs, vid):
    if thumbs:
        for t in reversed(thumbs):
            u = t.get("url") or ""
            if u and "vi_webp" not in u: return u
    return f"https://i.ytimg.com/vi/{vid}/hqdefault.jpg"

def _search_sync(query, limit=24):
    opts = _make_search_opts()
    with yt_dlp.YoutubeDL(opts) as ydl:
        info = ydl.extract_info(f"ytsearch{limit}:{query}", download=False)
    entries = info.get("entries", []) if info else []
    out = []
    for e in entries:
        if not e: continue
        vid = e.get("id") or e.get("video_id")
        if not vid: continue
        out.append({
            "id": vid, "title": e.get("title") or "Untitled",
            "channel": e.get("uploader") or e.get("channel") or "Unknown",
            "channel_id": e.get("channel_id") or e.get("uploader_id") or "",
            "duration": int(e.get("duration") or 0),
            "view_count": int(e.get("view_count") or 0),
            "thumbnail": _pick_thumb(e.get("thumbnails") or [], vid),
            "url": f"https://www.youtube.com/watch?v={vid}",
        })
    return out

def _stream_sync(video_id):
    url = f"https://www.youtube.com/watch?v={video_id}"
    opts = _make_stream_opts()
    with yt_dlp.YoutubeDL(opts) as ydl:
        info = ydl.extract_info(url, download=False)
    if not info: return None

    is_live = bool(info.get("is_live") or info.get("live_status") in ("is_live", "is_upcoming"))
    stream_url = info.get("url")
    formats = info.get("formats") or []

    if not stream_url:
        progressive = [f for f in formats
            if f.get("vcodec") and f["vcodec"] != "none"
            and f.get("acodec") and f["acodec"] != "none"
            and f.get("url")
            and (f.get("ext") == "mp4" or "mp4" in (f.get("container") or ""))]
        progressive.sort(key=lambda f: f.get("height") or 0, reverse=True)
        if progressive: stream_url = progressive[0]["url"]

    if not stream_url:
        any_combined = [f for f in formats
            if f.get("vcodec") and f["vcodec"] != "none"
            and f.get("acodec") and f["acodec"] != "none"
            and f.get("url")]
        any_combined.sort(key=lambda f: f.get("height") or 0, reverse=True)
        if any_combined: stream_url = any_combined[0]["url"]

    if not stream_url:
        for f in formats:
            if "m3u8" in (f.get("protocol") or "").lower() and f.get("url"):
                stream_url = f["url"]; break

    if not stream_url:
        stream_url = info.get("manifest_url") or info.get("hls_url")

    is_hls = bool(stream_url and ".m3u8" in stream_url)
    return {
        "stream_url": stream_url, "is_live": is_live, "is_hls": is_hls,
        "title": info.get("title"),
        "channel": info.get("uploader") or info.get("channel"),
        "channel_id": info.get("channel_id"),
        "duration": int(info.get("duration") or 0),
        "thumbnail": _pick_thumb(info.get("thumbnails") or [], info.get("id") or video_id),
        "description": info.get("description") or "",
        "view_count": int(info.get("view_count") or 0),
        "like_count": int(info.get("like_count") or 0),
        "upload_date": info.get("upload_date") or "",
    }

async def yt_search(query, limit=24, ttl=TTL_SEARCH):
    key = f"search::{query}::{limit}"
    hit = await cache_get(key)
    if hit is not None: return hit
    try:
        data = await _yt_call(_search_sync, query, limit)
    except Exception as e:
        logger.warning(f"search failed for '{query}': {str(e)[:200]}")
        return []
    if data: await cache_set(key, data, ttl)
    return data

_STREAM_LOCKS: dict = {}

def _get_stream_lock(video_id):
    lock = _STREAM_LOCKS.get(video_id)
    if lock is None:
        lock = asyncio.Lock(); _STREAM_LOCKS[video_id] = lock
    return lock

async def resolve_stream(video_id, force=False):
    key = f"stream::{video_id}"
    if not force:
        hit = await cache_get(key)
        if hit is not None: return hit
    lock = _get_stream_lock(video_id)
    async with lock:
        if not force:
            hit = await cache_get(key)
            if hit is not None: return hit
        try:
            data = await _yt_call(_stream_sync, video_id)
        except Exception as e:
            logger.warning(f"yt-dlp resolve failed for {video_id}: {str(e)[:200]}")
            data = None
        if data and data.get("stream_url"):
            await cache_set(key, data, TTL_STREAM)
        return data

