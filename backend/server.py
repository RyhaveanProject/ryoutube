_YT_SEMAPHORE = asyncio.Semaphore(2)
_LAST_CALL_TS = 0.0
_MIN_GAP = 1.0

async def _yt_call(fn, *args):
    global _LAST_CALL_TS
    async with _YT_SEMAPHORE:
        gap = time.time() - _LAST_CALL_TS
        if gap < _MIN_GAP:
            await asyncio.sleep(_MIN_GAP - gap)
        _LAST_CALL_TS = time.time()
        return await asyncio.to_thread(fn, *args)

